#For Loops

"""
for x in range(start,stop,step -> default = +1):
    print(x)
"""
#1,2,3 will have the same out come
for x in range(0,10,1):#1 start,stop and step
    print(x)

print()

for x in range(0,10):#2 start and stop
    print(x)

print()

for x in range(10):#3 only stop
    print(x)

print()

for x in range(0,10,2):
    print(x)
